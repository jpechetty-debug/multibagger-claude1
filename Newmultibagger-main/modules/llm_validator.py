if __name__ == "__main__":
    # Test vector simulating a database row
    test_vector = {
        "score": 85.0,
        "sales_growth": 25.4,
        "roe": 18.2,
        "pe_ratio": 30.5,
        "value_gap": -5.1,
    }

    # Simulating LLM hallucination (Invented 35.4% sales growth & 120 ROE)
    hallucinated_thesis = (
        "This company shows tremendous fundamental strength with an impressive 35.4% "
        "sales growth trajectory over the past year. Furthermore, it generates massive "
        "shareholder value operating at a 120% ROE. Finally, trading at just 30.2x earnings, "
        "it possesses an elite Sovereign score of 85."
    )

    validator = FactValidator(tolerance_pct=15.0)
    report = validator.validate(hallucinated_thesis, test_vector)

    print("--- Original LLM Output ---")
    print(hallucinated_thesis)

    print("\n--- Validation Audit Trail ---")
    for log in report.audit_trail:
        print(f">> {log}")

    print("\n--- Sanitized Output ---")
    patched = patch_thesis(hallucinated_thesis, report)
    print(patched)
