@router.get("/api/reports/html/{symbol}")
async def get_stock_report_html(symbol: str):
    """Serve the premium HTML report with cache busting."""
    try:
        from modules.html_report import generate_premium_html_report

        normalized_symbol = normalize_symbol(symbol)
        path = await generate_premium_html_report(normalized_symbol)
        if os.path.exists(path):
            return FileResponse(
                path,
                headers={
                    "Cache-Control": "no-store, no-cache, must-revalidate, max-age=0",
                    "Pragma": "no-cache",
                },
            )
        raise HTTPException(status_code=500, detail="Report generation failed")
    except HTTPException:
        raise
    except Exception as exc:
        raise HTTPException(
            status_code=500,
            detail=f"Failed to generate HTML report for {symbol}: {exc}",
        ) from exc


